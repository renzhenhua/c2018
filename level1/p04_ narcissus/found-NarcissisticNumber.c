#include<stdio.h>
#include<math.h>
int main()
{
	int ge_wei, shi_wei, bai_wei, x=3, i=100;
	for(i=100;i<=1000;i++){
		ge_wei = i % 10;
		shi_wei = (i % 100)/10;
		bai_wei = i/100;
		if(pow(ge_wei, x) + pow(shi_wei, x) + pow(bai_wei, x) == i){
			printf("%d is Narcissistic_number\n", i);
		}
	}
	return 0;
}
